

export const buildMatrix = (x, y) => {
    return Array(x)
    .fill()
    .map(() => Array(y).fill(0));
};


export const fillMatrix = (x, y) => {
    return Array(x)
    .fill()
    .map(() => Array(y).fill(1));
};

export const randomMatrix = (x, y) => {
    return Array(x)
    .fill()
    .map(() => Array(y).fill().map((x) => Math.round(Math.random())))
};


